    public static void rvPredictStart(Thread thread, int locId) {
        saveThreadSyncEvent(EventType.START, locId, thread.getId());
        metadata.addThreadCreationInfo(thread.getId(), Thread.currentThread().getId(), locId);
        thread.start();
    }

